      SUBROUTINE   RANKLW
     I                  (N,
     M                   X)
C
C     + + + PURPOSE + + +
C     sorts an array in ascending order, max of 120.
C
C     + + + DUMMY ARGUMENTS + + +
      INTEGER   N
      REAL      X(120)
C
C     + + + ARGUMENT DEFINITIONS + + +
C     N      - size of array to be sorted,
C              maximum of 120
C     X      - array to be sorted
C
C     + + + LOCAL VARIABLES + + +
      REAL Z(120), BIG, XMIN
      INTEGER   M,K,I,J,IK,IJ
C
C     + + + END SPECIFICATIONS + + +
C
      BIG = 1.0E29
      M=N
      DO 10 K=1,N
        Z(K)=X(K)
   10 CONTINUE
      I=1
 20   CONTINUE
        K=150
        XMIN=BIG
        DO 40 J=1,M
C         find minimum value in array
          IF (XMIN.GT.Z(J)) THEN
            K=J
            XMIN=Z(J)
          END IF
 40     CONTINUE
        X(I)=Z(K)
        IF (K.NE.M) THEN
C         remove found minimum value from array
          IK=K+1
          DO 50 J=IK,M
            IJ=J-1
            Z(IJ)=Z(J)
 50       CONTINUE
        END IF
        M=M-1
        I=I+1
      IF (I.LE.N) GO TO 20
C
      RETURN
      END
C
C
C
      SUBROUTINE   RANK
     I                  (N,
     M                   X)
C
C     + + + PURPOSE + + +
C     Sorts an array in descending order.  Maximum size of the
C     array is 120.
C
C     + + + DUMMY ARGUMENTS + + +
      INTEGER   N
      REAL      X(120)
C
C     + + + ARGUMENT DEFINITIONS + + +
C
C     + + + LOCAL VARIABLES + + +
      REAL Z(120), SMALL, XMAX
      INTEGER   M,K,I,J,IK,IJ
C
C     + + + END SPECIFICATIONS + + +
C
      SMALL = -1.0E29
      M=N
      DO 10 K=1,N
        Z(K)=X(K)
   10 CONTINUE
      I=1
 20   CONTINUE
        K=150
        XMAX=SMALL
        DO 40 J=1,M
          IF (XMAX.LT.Z(J)) THEN
            K=J
            XMAX=Z(J)
          END IF
 40     CONTINUE
        X(I)=Z(K)
        IF (K.NE.M) THEN
          IK=K+1
          DO 50 J=IK,M
            IJ=J-1
            Z(IJ)=Z(J)
 50       CONTINUE
        END IF
        M=M-1
        I=I+1
      IF (I.LE.N) GO TO 20
C
      RETURN
      END
