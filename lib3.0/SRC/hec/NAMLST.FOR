      SUBROUTINE NAMLST(CNAMES,INAMES)
C
C     NAMLST IS ASSOCIATED WITH SUBROUTINES TRUNAM, AND SETNAM.
C     THIS ROUTINE WRITES THE NAMES IN LIST PROVIDED (NAMES) TO LFN 6.
C
      CHARACTER CNAMES*(*)
      DIMENSION CNAMES(*)
      CHARACTER CDAT*7, CTIM*8
      DIMENSION INAMES(-4:*)
C
      NTRU = INAMES(-2)
      NPSU = INAMES(-1)
      MAX = INAMES(-4)
      ICURR = INAMES(-3)
      CALL CTIME(CTIM)
      CALL CDATE(CDAT)
C
C     WRITE HEADINGS, AND PERTIENT INFORMATION
C
      WRITE(6,10)
      WRITE(6,20)CDAT(1:7),CTIM(1:8)
      WRITE(6,30)NTRU,NPSU,ICURR,MAX
      WRITE(6,40)
C
C     LOOK FOR TRUE NAMES IN THE LIST
C
      KK = INAMES(-4)
      DO 60 I=1,KK
          IF (I.NE.INAMES(I)) GO TO 60
C         ***  FOUND ONE  ***
             WRITE(6,70)CNAMES(I)
C            ****  LOOK FOR ITS PSUEDO'S  ****
      NN = INAMES(-4)
             DO 50 K=1,NN
               IF (INAMES(K).NE.I.OR.K.EQ.I) GO TO 50
               WRITE(6,80)CNAMES(K)
50           CONTINUE
60    CONTINUE
      WRITE(6,90)
      RETURN
C
10    FORMAT('1',///,T2,80('*'),//,T25,'*****   NAME ARRAY LISTING   ',
     *'*****')
20    FORMAT(//,' DATE:  ',A7,/' TIME:  ',A8)
30    FORMAT(//,' NUMBER OF TRUE NAMES   =',I4,/,
     *' NUMBER OF PSUEDO NAMES =',I4,/,
     *' CURRENT LIST LENGTH    =',I4,/,
     *' MAXIMUM LIST LENGTH    =',I4)
40    FORMAT(//,T4,'TRUE NAME',T40,'PSUEDO NAMES')
70    FORMAT(/,T4,A)
80    FORMAT(T40,A)
90    FORMAT(//,T2,80('*'))
      END
