      SUBROUTINE CJSTR ( CSTR1, NBEG1, NLEN1, CSTR2, NBEG2)
C
C     CENTER JUSTIFY STRING CSTR1 INTO CSTR2
C
      CHARACTER CSTR1*(*), CSTR2*(*)
C
      CALL LFLNB (CSTR1,NBEG1,NLEN1,K1,K2)
      IF (K1.EQ.0) GO TO 10
      I = (NLEN1-K2)/2 + NBEG2
      CSTR2(I:I+K2-1) = CSTR1(K1:K1+K2-1)
      J = I - NBEG2
      IF (J.GT.0) CSTR2(NBEG2:NBEG2+J-1) = ' '
      J = I + K2
      IF (J.LT.NLEN1+NBEG2) CSTR2(J:NBEG2+NLEN1-1) = ' '
      RETURN
C
C     IA IS BLANK - BLANK IB
 10   CSTR2(NBEG2:NBEG2+NLEN1-1) = ' '
      RETURN
      END
